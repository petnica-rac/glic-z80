;------------------------------------------------------------------------------
; Brisanje ekrana.
;------------------------------------------------------------------------------

cls:
	push	hl
	
	ld	hl,$7800	; Početak VRAM-a
.loop:
	ld	(hl),0		; Obriši bajt
	inc	hl		; Pređi na sledeći
	bit	7,h		; Da li se prešlo na 32K+1?
	jr	z,.loop		; Ako nije nastavi da brišeš

	pop	hl
	ret	
	
;------------------------------------------------------------------------------
; Ispis stringa na adresu VRAM.
; Ulaz:
;	DE - adresa u VRAM od koje počinje upis
;	HL - adresa NULL terminiranog string koji se ispisuje
; Izlaz:
;	DE - pokazuje na sledeću poziciju za ispis novog stringa
;------------------------------------------------------------------------------

print_str:
	push	af
	push	hl
.loop:
	ld	a,(hl)		; Učitaj karakter
	cp	0		; Da li je NULL?
	jr	z,.exit		; Ako jeste kraj
	call	print_char	; Ispiši karakter
	inc	hl		; Pređi na sledeći karakter
	jr	.loop		; Nastavi sa stringom
.exit:
	pop	hl
	pop	af
	ret

;------------------------------------------------------------------------------
; Ispis jednog karaktera u VRAM.
; Ulaz:
; 	A - karakter koji treba ispisati, bit 7 znači inverzni ispis
;	DE - adresa na ekranu od koje se ispisuje karakter
; Izlaz:
;	DE - uvećan da pokazuje na sledeću poziciju za novi karakter u VRAM
;------------------------------------------------------------------------------

print_char:
	
	push	af
	push	bc
	push	hl

	; Ako je bit 7 setovan na .INV poziciju upiši CPL instrukciju,
	; a ako nije podrazumevano je NOP. Ovde program menja samog sebe:
	
	ld	hl,.inv		; Pozicija instrukcije koja se menja
	ld	(hl),$00	; Resetuj da bude NOP
	bit	7,a		; Provera bita 7
	jr	z,.skip_cpl	; Ako je nula preskoči promenu instrukcije
	ld	(hl),$2F	; Postavi instrukciju CPL
.skip_cpl:

	; Pripremi kod karaktera u BC kao priprema za izračunavanje ofset adrese.
	; Bit 7 se briše da se ispis ograniči na prepis iz tabele sa definicijom fonta
	; za 128 karaktera (1K podataka):
	
	and	%01111111	; Obriši bit 7
	ld	b,0		; Pripremi BC za izračunavanje ofseta	
	ld	c,a
	
	; Pomnoži BC sa 8 (tri puta pomeri BC za bit, sa Carry između B i C):

	rl	c 		; Pomeri bitove u levo, bit 7 u Carry
	rl	b		; Pomeri bitove u levo, bit 0 iz Carry
	ld	a,c		; Obriši bit 0 u C (došao nepotrebno iz Carry)
	and	%11111110
	ld	c,a
	rl	c 		; Pomeri bitove u levo, bit 7 u Carry
	rl	b		; Pomeri bitove u levo, bit 0 iz Carry
	ld	a,c		; Obriši bit 0 u C (došao nepotrebno iz Carry)
	and	%11111110
	ld	c,a
	rl	c 		; Pomeri bitove u levo, bit 7 u Carry
	rl	b		; Pomeri bitove u levo, bit 0 iz Carry
	ld	a,c		; Obriši bit 0 u C (došao nepotrebno iz Carry)
	and	%11111110
	ld	c,a
	
	; Kopiraj bajtove iz definicije karaktera u VRAM:
	
	ld	hl,font_data4	; Učitaj početak definicije izgleda karaktera
	add	hl,bc		; Dodaj ofset karaktera koji treba ispisati
	
	ld	bc,(font_width)	; Podesi brojač bajtova za ispis (koristi se samo B)
.loop:
	ld	a,(hl)		; Uzmi bajt definicije karaktera
.inv:	nop			; Ova instrukcija se zameni sa CPL ako je inverz
	ld	(de),a		; Prebaci ga u VRAM
	inc	de		; Sledeća adresa u VRAM
	inc	hl		; Sledeći bajt definicije karaktera
	djnz	.loop		; Nastavi za svih 8 bajtova
.exit:
	pop	hl
	pop	bc
	pop	af
	ret
	
	; Broj bajtova koji čine karakter u definiciji. Može biti proizvoljna širina,
	; podrazumevano je 8, ali se ovaj parametar može promeniti. Stavljena su dva
	; bajta da bi se mogla koristiti instrukcija LD BC,(NN) u rutini, pošto
	; instrukcija LD B,(NN) ne postoji:
	
font_width:
	db	0		; Niži bajt, nepotreban (ide u C)
	db	4		; Viši bajt, širina karaktera u font tabeli (ide u B)

;------------------------------------------------------------------------------
; Test celog fonta.
;------------------------------------------------------------------------------

testfont:
	push	af
	push	bc
	push	de
	push	hl
	
	; Ispis fonta u direkt i inverz modu:
	
	ld	a,0		; Brojač koda karaktera
	ld	de,$7800	; Početak VRAM
.font_loop:
	call	print_char	; Prikaži karakter
	inc 	a		; Sledeći karakter
	jr	nz,.font_loop	; Nastavi ako nije prošao kroz celu tabelu	
	
	pop	hl
	pop	de
	pop	bc
	pop	af
	
	ret
		
;------------------------------------------------------------------------------
; Čeka na taster
;------------------------------------------------------------------------------

wait_key:

	push	af
	
	; Čekanje da se tasteri pritisnu:
	
.wait_press:
	in	a,(0)
	cp	255
	jr	z,.wait_press
	
	; Čekanje da se tastri puste ako su pritisnuti:
	
.wait_rel:
	in	a,(0)
	cp	255
	jr	nz,.wait_rel
	
	pop	af
	ret

